package com.ufharmony.grid;

public class Skin_TextureLocation
{
  private int column;
  private int row;

  public Skin_TextureLocation(int column, int row)
  {
    this.column = column;
    this.row = row;
  }

  public int getColumn()
  {
    return column;
  }

  public int getRow() {
    return row;
  }
}